""" Goal: Understand how to use variables in Python """

# Declare and initialize variables
name = "John"
age = 25
height = 1.75

# Print the values of the variables
print("Name:", name)
print("Age:", age)
print("Height:", height)

# Example variables using f-strings
print(f"My name is {name}, I am {age} years old, and my height is {height} meters.")

# Update variables
name = "Brian"
age = 30
height = 1.80

# Print the updated values of the variables
print("Updated Name:", name)
print("Updated Age:", age)
print("Updated Height:", height)

# Example variables using f-strings
print(f"My name is {name}, I am {age} years old, and my height is {height} meters.")